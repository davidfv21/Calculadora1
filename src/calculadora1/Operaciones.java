public class Operaciones {
    float a;
    float b;
    float Sumar(){
        float Resultado;
        Resultado = a+b;
        return Resultado;
    }
     float Restar(){
        float Resultado;
        Resultado = a-b;
        return Resultado;
    }
      float Multiplicar(){
        float Resultado;
        Resultado = a*b;
        return Resultado;
    }
       float Dividir(){
        float Resultado;
        Resultado = a/b;
        return Resultado;
    }
}
